--사원 : 사번, 이름, 비밀번호, 직급, 팀, 휴대전화, 이메일, 주소, 입사일, 재직현황(재직Y 퇴사N), 사진경로
CREATE TABLE EMPLOYEE (
	EMP_CODE NUMBER PRIMARY KEY,
	EMP_NAME VARCHAR2(40) NOT NULL,
	EMP_PW VARCHAR2(500) NOT NULL,
	DEPT_CODE NUMBER NOT NULL,
	JOB_CODE NUMBER NOT NULL,
	EMP_TEL VARCHAR2(20),
	EMP_EMAIL VARCHAR2(500),
	EMP_ADDRESS VARCHAR2(500),
	GENDER CHAR(1),
	JOINDATE DATE,
	EMP_STATUS CHAR(1) DEFAULT 'Y',
	EMP_IMG VARCHAR2(2000),
	EMP_SIG VARCHAR2(2000),
	EMP_AUTH CHAR(1)
);

--부서 : 부서번호, 부서명
CREATE TABLE DEPARTMENT(
	DEPT_CODE NUMBER PRIMARY KEY,
	DEPT_NAME VARCHAR2(30) NOT NULL
);

--직급 : 직급번호, 직급명
CREATE TABLE JOB(
	JOB_CODE NUMBER PRIMARY KEY,
	JOB_NAME VARCHAR2(30) NOT NULL
);

--연차 : 사번, 남은 연차, 사용 연차
CREATE TABLE DAYOFF(
	EMP_CODE NUMBER PRIMARY KEY,
	DAYOFF_LEFT NUMBER NOT NULL,
	DAYOFF_USED NUMBER NOT NULL
);

--출장 : 출장번호, 사번, 출장지, 상세, 기간(시작, 끝), 차량
CREATE TABLE BTRIP(
	BTRIP_CODE NUMBER PRIMARY KEY,
	EMP_CODE NUMBER NOT NULL,
	BTRIP_DEPART VARCHAR2(500) DEFAULT '서울특별시 금천구 가산디지털2로 95',
	BTRIP_ARRIVAL VARCHAR2(500),
	BTRIP_DETAIL VARCHAR2(3000) DEFAULT '업무상 출장을 명령받음',
	BTRIP_START_DATE DATE,
	BTRIP_END_DATE DATE,
	VEHICLE_CODE NUMBER
);

--회의실 : 회의실 번호, 회의실 이름, 회의실 크기
CREATE TABLE MEETROOM(
	MEETROOM_CODE NUMBER PRIMARY KEY,
	MEETROOM_NAME VARCHAR2(30) NOT NULL,
	MEETROOM_SIZE NUMBER NOT NULL
);

--회의실 예약 : 예약번호, 회의실 번호, 사원번호, 날짜
CREATE TABLE MRRESERVATION(
	MRR_CODE NUMBER PRIMARY KEY,
	EMP_CODE NUMBER NOT NULL,
	MEETROOM_CODE NUMBER NOT NULL,
	MRR_STARTTIME DATE,
	MRR_ENDTIME DATE
);

--회의록
CREATE TABLE MINUTES (
    MINUTES_CODE NUMBER PRIMARY KEY,
    MINUTES_DATE DATE,
    MEETROOM_CODE NUMBER,
    EMP_CODE NUMBER,
    MINUTES_ATTENDEES VARCHAR2(2000),
    MINUTES_SUBJECT VARCHAR2(200),
    MINUTES_CONTENT CLOB,
    MINUTES_SUMMARY CLOB
);

-- 차량 : 차량 등록 번호, 차량번호, 차종, 배차, 등록일자, 구분(영업용C, 화물용F), 인승, 현황(삭제Y 사용중N), 사진, 상태(출차O, 보관I, 수리R)
CREATE TABLE VEHICLE(
	VEHICLE_CODE NUMBER PRIMARY KEY,
	VEHICLE_NO VARCHAR2(30) NOT NULL,
	VEHICLE_MODEL VARCHAR2(30) NOT NULL,
	VEHICLE_REGDATE DATE,
	VEHICLE_TYPE CHAR(1) DEFAULT 'C' NOT NULL,
	VEHICLE_SIZE NUMBER,
	VEHICLE_DELFLAG CHAR(1) DEFAULT 'N' NOT NULL,
	VEHICLE_IMG VARCHAR2(2000),
	VEHICLE_STATUS CHAR(1)
);

--수리 : 수리내역 번호, 차량 번호, 수리내역, 수리일자, 상세, 수리현황
CREATE TABLE REPAIR(
	REPAIR_CODE NUMBER PRIMARY KEY,
	VEHICLE_CODE NUMBER,
	REPAIR_DETAIL VARCHAR2(300) DEFAULT '엔진 수리',
	REPAIR_DATE DATE,
	REPAIR_REASON VARCHAR2(300) DEFAULT '정기 검진 중 발견한 결함 수리',
	REPAIR_STATUS CHAR(1)
);

-- 운송 : 운송 번호, 출발지, 목적지, (화물은 input입력) 차량번호, 출발 날짜, 도착 날짜, 상태, 사원번호, 작성자
CREATE TABLE DELIVERY(
	DELIVERY_CODE NUMBER PRIMARY KEY,
	DELIVERY_DEPART VARCHAR2(300),
	DELIVERY_ARRIVAL VARCHAR2(300),
	FREIGHT_CODE NUMBER,
	VEHICLE_CODE NUMBER,
	DELIVERY_STARTTIME DATE,
	DELIVERY_ENDTIME DATE,
	EMP_CODE NUMBER,
	DELIVERY_WEIGHT NUMBER,
	DELIVERY_STATUS CHAR(1)
);

--화물 : 화물 번호, 화물 이름, 화물 상세설명 
CREATE TABLE FREIGHT(
	FREIGHT_CODE NUMBER PRIMARY KEY,
	FREIGHT_NAME VARCHAR2(300)
);

-- 협력 업체 : 사업체 번호, 회사명, 주소, 전화번호, 협력업체 담당자 이름
CREATE TABLE COWORK(
	COW_CODE NUMBER PRIMARY KEY,
	COW_NAME VARCHAR2(300),
	COW_ADDRESS VARCHAR2(1000),
	COW_TEL VARCHAR2(20),
	COW_MANAGER VARCHAR2(30),
	COW_MGRJOB VARCHAR2(30),
	COW_REGDATE DATE,
	COW_DELFLAG CHAR(1) DEFAULT 'N'
);

-- 배차 신청서 : 배차신청번호, 출장번호, 차량번호, 배차신청사유
CREATE TABLE VEHICLERESERVATION(
	VRSV_CODE NUMBER PRIMARY KEY,
	BTRIP_CODE NUMBER,
	VEHICLE_CODE NUMBER,
	VRSV_DATE VARCHAR2(100),
	VRSV_DETAIL VARCHAR2(1500),
	VRSV_STATUS CHAR(1),
	VRSV_REPLY VARCHAR2(300)
);

--전자 결재 : 문서 번호, 문서 제목, 문서 내용, 사번, 결재 일자, 상태
CREATE TABLE EDOC(
	EDOC_CODE NUMBER PRIMARY KEY,
	EDOC_TYPE CHAR(1),
	EDOC_TITLE VARCHAR2(300),
	EDOC_CONTENT VARCHAR2(4000),
	EMP_CODE NUMBER NOT NULL,
	EDOC_DATE DATE,
	EDOC_STATUS CHAR(1),
	EDOC_REPLY VARCHAR2(2000),
	EDOC_RECEIPT_IMG VARCHAR2(300)
);

--결재선 : 결재선코드, 문서번호, 사원번호, 결재현황
CREATE TABLE EDOCLINE(
	EDCL_CODE NUMBER PRIMARY KEY,
	EDOC_CODE NUMBER,
	EMP_CODE NUMBER,
	EDCL_STATUS CHAR(1)
);

--공지사항: 글번호, 제목, 내용, 작성자, 작성일자, 삭제 여부(삭제Y 유지N), 중요상태
CREATE TABLE NOTICE(
	NOTI_CODE NUMBER PRIMARY KEY,
	NOTI_TITLE VARCHAR2(300),
	NOTI_CONTENT CLOB,
	EMP_CODE NUMBER,
	NOTI_DATE DATE,
	NOTI_DELFLAG CHAR(1) DEFAULT 'N',
	NOTI_STATUS CHAR(1)
);

--파일 게시판: 파일번호, 공지사항 글번호, 파일원본명, 파일수정명, 폴더경로, 업로드일, 첨부파일타입, 파일 사이즈, 삭제
CREATE TABLE NOTICEFILE(
	NFILE_CODE NUMBER PRIMARY KEY,
	NOTI_CODE NUMBER,
	NFILE_ORIGINNAME VARCHAR2(2000),
	NFILE_FAKENAME VARCHAR2(2000),
	NFILE_PATH VARCHAR2(2000),
	NFILE_DATE DATE,
	NFILE_TYPE VARCHAR2(30),
	NOTI_SIZE NUMBER,
	NFILE_DELFLAG CHAR(1) DEFAULT 'N'
);

--채팅방 : 채팅방번호, 채팅방 생성일자
CREATE TABLE CHATROOM(
	CHATROOM_CODE NUMBER PRIMARY KEY,
	CHATROOM_PARTI VARCHAR2(200),
	CHATROOM_CREDTEDAT DATE
);

--채팅: 채팅ID, 채팅방 번호, 내용, 발신자, 시간
CREATE TABLE CHAT(
	CHAT_CODE NUMBER PRIMARY KEY,
	CHATROOM_CODE NUMBER,
	CHAT_CONTENT VARCHAR2(2000),
	CHAT_SENDER VARCHAR2(60),
	CHAT_TIME DATE
);

--채팅 파일: 파일 ID, 채팅방ID, 파일이름, 파일경로, 파일크기, 업로드일자
CREATE TABLE CHATFILE(
	CFILE_CODE NUMBER PRIMARY KEY,
	CHAT_CODE NUMBER,
	CFILE_NAME VARCHAR2(90),
	CFILE_PATH VARCHAR2(2000),
	CFILE_SIZE NUMBER,
	CFILE_DATE DATE
);

